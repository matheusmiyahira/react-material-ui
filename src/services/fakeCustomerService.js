export function getCustomers() {
  return customers;
}

export function getCustomer(id) {
  return customers.find(m => m._id === id);
}

export function saveCustomer(customer) {
  let customerInDb = customers.find(m => m._id === customer._id) || {};
  customerInDb.name = customer.name;
  customerInDb.nickname = customer.nickname;
  customerInDb.email = customer.email;
  customerInDb.phone = customer.phone;
  customerInDb.address.street = customer.address.street;
  customerInDb.address.suite = customer.address.suite;
  customerInDb.address.city = customer.address.city;
  customerInDb.address.zipcode = customer.address.zipcode;

  if (!customerInDb._id) {
    customerInDb._id = Date.now().toString();
    customers.push(customerInDb);
  }

  return customerInDb;
}

export function deleteCustomer(id) {
  let customerInDb = customers.find(m => m._id === id);
  customers.splice(customers.indexOf(customerInDb), 1);
  return customerInDb;
}
